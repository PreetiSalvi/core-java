package edu.java.core;

public class BindingTypes {

    public void whatTypesofBInding(){
        System.out.println(" There are 2 types of binding : Static & Dynamic");
        System.out.println(" Static ==> type of the object is determined at compiled time(by the compiler),");
        System.out.println(" Static ==> Method call and body connection decided during compile time");
        System.out.println(" Static ==> ANYTHING that is private , final and static");
        System.out.println(" Dynamic ==> Method call and body connection decided during run time");
    }


}
